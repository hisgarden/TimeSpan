#!/bin/bash

# Setup script for pre-commit hooks
# This script installs the pre-commit hook to run tests before commits

set -e

echo "ðŸ”§ Setting up pre-commit hooks for TimeSpan..."

# Check if we're in a git repository
if ! git rev-parse --git-dir > /dev/null 2>&1; then
    echo "âŒ Not in a git repository. Please run this from the TimeSpan repository root."
    exit 1
fi

# Get the repository root
REPO_ROOT=$(git rev-parse --show-toplevel)
HOOKS_DIR="$REPO_ROOT/.git/hooks"
PRE_COMMIT_HOOK="$HOOKS_DIR/pre-commit"
SCRIPT_PATH="$REPO_ROOT/scripts/pre-commit.sh"

echo "ðŸ“ Repository root: $REPO_ROOT"
echo "ðŸ“ Hooks directory: $HOOKS_DIR"

# Check if the pre-commit script exists
if [ ! -f "$SCRIPT_PATH" ]; then
    echo "âŒ Pre-commit script not found at $SCRIPT_PATH"
    exit 1
fi

# Make sure the script is executable
chmod +x "$SCRIPT_PATH"

# Create the pre-commit hook
echo "ðŸ“ Creating pre-commit hook..."
cat > "$PRE_COMMIT_HOOK" << EOF
#!/bin/bash
# Pre-commit hook for TimeSpan
# Generated by setup-pre-commit.sh

exec "$SCRIPT_PATH"
EOF

# Make the hook executable
chmod +x "$PRE_COMMIT_HOOK"

echo "âœ… Pre-commit hook installed successfully!"
